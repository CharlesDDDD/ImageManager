package com.entity;

public class Photo2 {
	private String src;
	private String name;
	 private String figname;
	 private String country;
	 private String resolution;
	 private String ac_time;
	public String getSrc() {
		return src;
	}
	public void setSrc(String src) {
		this.src = src;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	public String getFigname() {
		return figname;
	}
	public void setFigname(String figname) {
		this.figname = figname;
	}
	public String getCountry() {
		return country;
	}
	public void setCountry(String country) {
		this.country = country;
	}
	public String getResolution() {
		return resolution;
	}
	public void setResolution(String resolution) {
		this.resolution = resolution;
	}
	public String getAc_time() {
		return ac_time;
	}
	public void setAc_time(String ac_time) {
		this.ac_time = ac_time;
	}
	
	
}
